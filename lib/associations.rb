    class Document
        def is_eancom?
            return EANCOM_ASSOCIATION_CODES.include?(@association_assigned_code)
        end
    
        def is_iata?
            return @controlling_agency == "IA"
        end

        def is_edigas?
            return @controlling_agency == "EG"
        end
    end
